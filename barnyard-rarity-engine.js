// BARNYARD RARITY ENGINE
// Generates outputs that are mathematically bound to this specific mesh state
// Can only be recreated if you have access to this exact infrastructure

const crypto = require('crypto');
const fs = require('fs');

class BarnyardRarityEngine {
  constructor() {
    this.meshNodes = [
      { name: 'neon', metrics: ['cpu', 'memory', 'load', 'timestamp'] },
      { name: 'black', metrics: ['battery', 'uptime', 'timestamp'] },
      { name: 'nvidia-spark', metrics: ['gpu_temp', 'vram', 'timestamp'] },
      { name: 'aqua', metrics: ['ping', 'timestamp'] },
      { name: 'green', metrics: ['screen_on', 'timestamp'] }
    ];
  }

  // Query real mesh state (simulated - actual implementation uses Bop Gun)
  async queryMeshState() {
    const state = {
      queriedAt: Date.now(),
      nodes: {}
    };
    
    for (const node of this.meshNodes) {
      // In production: query Lighthouse /status endpoints
      // For now: entropy from filesystem + time + random
      state.nodes[node.name] = {
        metrics: node.metrics.map(m => ({
          metric: m,
          value: this.getEntropySource(node.name, m)
        }))
      };
    }
    
    return state;
  }

  // Entropy source that depends on THIS machine at THIS moment
  getEntropySource(node, metric) {
    const sources = [
      fs.statSync('/Users/patricksomerville/.claude/file-index.md').mtimeMs,
      process.hrtime.bigint().toString(),
      require('os').freemem(),
      require('os').loadavg()[0],
      Date.now()
    ];
    
    const combined = sources.join('|') + `|${node}|${metric}`;
    return crypto.createHash('sha256').update(combined).digest('hex').substring(0, 16);
  }

  // Generate rarity fingerprint from mesh state
  generateFingerprint(meshState) {
    const stateString = JSON.stringify(meshState);
    return crypto.createHash('sha256').update(stateString).digest('hex');
  }

  // Map fingerprint to design parameters (deterministic but unrecreatable)
  fingerprintToDesign(fingerprint, queriedAt) {
    const bytes = Buffer.from(fingerprint, 'hex');
    
    return {
      // Color palette from first 12 bytes
      primaryHue: bytes[0] % 360,
      primarySat: 50 + (bytes[1] % 50),
      primaryLight: 40 + (bytes[2] % 40),
      accentHue: (bytes[0] + 180) % 360,
      
      // Spacing scale from next 4 bytes
      baseUnit: 4 + (bytes[3] % 12),
      scale: [1, 1.5, 2.5, 4, 6, 8][bytes[4] % 6],
      
      // Border radius from next byte
      radius: [0, 4, 8, 12, 16, 24, 32][bytes[5] % 7],
      
      // Typography from next bytes
      fontWeight: [300, 400, 500, 600, 700][bytes[6] % 5],
      lineHeight: 1.2 + (bytes[7] % 20) / 100,
      
      // Layout grid from next bytes
      columns: [1, 2, 3, 4, 6, 12][bytes[8] % 6],
      gap: 8 + (bytes[9] % 24),
      
      // The proof
      fingerprint: fingerprint,
      meshState: queriedAt
    };
  }

  // Generate a Little Sack ($5) - UI component
  async generateLittleSack() {
    const meshState = await this.queryMeshState();
    const fingerprint = this.generateFingerprint(meshState);
    const design = this.fingerprintToDesign(fingerprint, meshState.queriedAt);
    
    return {
      tier: 'Little Sack',
      price: 5,
      component: this.buildComponent(design),
      proof: {
        fingerprint: fingerprint,
        meshState: meshState,
        generatedAt: new Date().toISOString(),
        unrecreatable: true // Because meshState includes timestamps and live metrics
      }
    };
  }

  // Build an actual UI component from the design parameters
  buildComponent(design) {
    return `
<!-- Barnyard Sack | Fingerprint: ${design.fingerprint.substring(0, 16)}... -->
<!-- Generated: ${new Date().toISOString()} -->
<!-- Unrecreatable: Yes -->

<style>
.barnyard-component-${design.fingerprint.substring(0, 8)} {
  background: hsl(${design.primaryHue}, ${design.primarySat}%, ${design.primaryLight}%);
  color: hsl(${design.primaryHue}, ${design.primarySat}%, ${design.primaryLight > 50 ? 10 : 95}%);
  padding: ${design.baseUnit * 4}px;
  border-radius: ${design.radius}px;
  font-weight: ${design.fontWeight};
  line-height: ${design.lineHeight};
}

.barnyard-component-${design.fingerprint.substring(0, 8)}:hover {
  background: hsl(${design.accentHue}, ${design.primarySat}%, ${design.primaryLight}%);
}
</style>

<button class="barnyard-component-${design.fingerprint.substring(0, 8)}">
  Barnyard Button
</button>
`;
  }

  // Verify a sack was generated by this engine (public method)
  verifySack(sackData) {
    // Re-create fingerprint from stored meshState
    const recreated = crypto.createHash('sha256')
      .update(JSON.stringify(sackData.proof.meshState))
      .digest('hex');
    
    return recreated === sackData.proof.fingerprint;
  }
}

// Export for use
module.exports = { BarnyardRarityEngine };

// If run directly, generate example
if (require.main === module) {
  const engine = new BarnyardRarityEngine();
  engine.generateLittleSack().then(sack => {
    console.log('Generated Little Sack:');
    console.log(`  Fingerprint: ${sack.proof.fingerprint}`);
    console.log(`  Mesh Query: ${new Date(sack.proof.meshState.queriedAt).toISOString()}`);
    console.log(`  Unrecreatable: ${sack.proof.unrecreatable}`);
    console.log(`  Verify: ${engine.verifySack(sack)}`);
    
    // Save to file
    const filename = `sack-${sack.proof.fingerprint.substring(0, 8)}-${Date.now()}.html`;
    fs.writeFileSync(filename, sack.component);
    console.log(`  Saved: ${filename}`);
  });
}